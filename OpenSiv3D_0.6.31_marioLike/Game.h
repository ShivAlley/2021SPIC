struct Parameter
{
	Parameter() = default;
	template <typename T>
	Parameter(T x,T y,T accel,T jump)
		:MAX_VELOCITY_X(x)
		,MAX_VELOCITY_Y(y)
		,ACCELERATION_X(accel)
		,JUMP_POWER(jump)
	{}
	template <class T>
	Parameter(T v1,T v2)
		:MAX_VELOCITY_X(v1.x)
		,MAX_VELOCITY_Y(v1.y)
		,ACCELERATION_X(v2.x)
		,JUMP_POWER(v2.y)
	{}

	double MAX_VELOCITY_X;
	double MAX_VELOCITY_Y;
	double ACCELERATION_X;
	double JUMP_POWER;
};

