template <class T>
concept vector = requires (T& x) {
	x.x;
	x.y;
};


struct Parameter
{
	Parameter() = default;
	template<vector v,typename T>
	Parameter(v v1, v v2,T d1, v v3)
		:maxVelocity(v1)
		,accelerationX(v2.x)
		,jumpPower(v2.y)
		,floorKickPower(d1)
		, wallKickPower(v3)
	{}

	Vec2 maxVelocity;
	Vec2 wallKickPower;
	double accelerationX;
	double jumpPower;
	double floorKickPower;
};

